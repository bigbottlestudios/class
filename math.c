#include <stdio.h>

int Add2Numbers(int b, int a) {
    int result = b + a;

    return result;
}




void Sub2Numbers(int b, int a) {
    int result = b - a;

    printf("%d\n", result);
}
void Mul2Numbers(int b, int a) {
    int result = b * a;

    printf("%d\n", result);
}
void Div2Numbers(int b, int a) {
    int result = b / a;

    printf("%d\n", result);
}